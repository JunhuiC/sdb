package org.continuent.sequoia.controller.backend.result;

import java.io.Serializable;

/**
 * This class stores the result of a call to Statement.executeUpdate(sql,
 * Statement.RETURN_GENERATED_KEYS);
 * 
 * @author <a href="mailto:emmanuel.cecchet@continuent.com">Emmanuel Cecchet</a>
 * @version 1.0
 */
public class GeneratedKeysResult extends AbstractResult implements Serializable
{
  private static final long   serialVersionUID = 8070578069695378440L;

  private int                 updateCount      = 0;
  private ControllerResultSet crs;

  /**
   * Creates a new <code>GeneratedKeysResult</code> object
   * 
   * @param crs the ControllerResultSet containing the autogenerated keys
   * @param updateCount the update count returned by the executeUpdate exection
   */
  public GeneratedKeysResult(ControllerResultSet crs, int updateCount)
  {
    this.crs = crs;
    this.updateCount = updateCount;
  }

  /**
   * Returns the ControllerResultSet value.
   * 
   * @return Returns the crs.
   */
  public final ControllerResultSet getControllerResultSet()
  {
    return crs;
  }

  /**
   * Returns the updateCount value.
   * 
   * @return Returns the updateCount.
   */
  public final int getUpdateCount()
  {
    return updateCount;
  }

}
